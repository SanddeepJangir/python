# 1 to 20 print multiples of 3
i = 1
while i<=20:
    if(i%3==0):
        print(i, end=" ")
    i+=1
print()

# 20 to 1 print multiples of 3 for loop
for i in range(1, 20 ):
    if (i%3==0):
        print(i, end=" ")
print()

# 100 to 1 print multiples of 7 while loop
i = 100
while i>=1:
    if(i%7==0):
        print(i, end=" ")
    i-=1
print()

# 100 to 1 print multiples of 7 for loop
for i in range(100, 1, -1):
    if(i%7==0):
        print(i, end=" ")
print()





