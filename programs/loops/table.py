# print table (2*1=2....2*10=20) for loop
for i in range(1, 11):
    print(2 ,"x", i ,"=", 2*i)
print()

# while loop print table of 3
i = 1
while i<=10:
    print(3, "x", i ,"=", 3*i)
    i+=1
print()

# print table in reverse order for loop
for i in range(10, 0, -1):
    print(2, "x", i, "=", 2*i)
print()

# print table in reverse order while loop
i = 10
while i>=1:
    print(3, "x", i, "=", 3*i)
    i-=1