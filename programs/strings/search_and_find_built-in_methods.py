# find(): search the string for specified value and return position of where it was found.
a = "Hello, welcome to my world to."
print(a.find("welcome"))

# rfind(): search the string for specified value and return the last position of where it was found. (last index)
b = "Hello, welcome to my world to."
print(b.rfind("to"))

# index(): search the string for specified value and return the position of where it was found.
c = "Hello, welcome to my world to."
print(c.index("welcome"))

# count(): returns the no. of times a specified value occurs in a string.
d = "Hello, welcome to my world to."
print(d.count("to"))