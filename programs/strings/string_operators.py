# concatenation (+): to add the strings with (+) operator.
a = "Avani"
b = "Sharma"
c = a+b
print(c)
d = a+ " " +b
print(d)

# repeating string (*): repeat a string multiple times using * operator.
a = "Avani"
print(a*3)

# string iteration (Membership operator):
s = "Python"
for char in s:
    print(char)